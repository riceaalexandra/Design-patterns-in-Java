package h2_3;

public class C3Nr {
    private int n1;
    private int n2;
    private int n3;

    public C3Nr(int n1, int n2, int n3) {
        this.n1 = n1;
        this.n2 = n2;
        this.n3 = n3;
    }
    public  C3Nr(){

    }
    public int sum(int n1, int n2, int n3) {
        return n1 + n2+n3;
    }

}
